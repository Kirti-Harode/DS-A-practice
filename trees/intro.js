// Tree Traversal algorithms can be classified broadly in two categories:

// 1) Depth-First Search (DFS) Algorithms
// 2) Breadth-First Search (BFS) Algorithms

//1) Depth-First Search (DFS) Algorithms have three variants:

// a. Preorder Traversal (current-left-right)— Visit the current node before visiting any nodes inside left or right subtrees.
// b. Inorder Traversal (left-current-right)— Visit the current node after visiting all nodes inside left subtree but before visiting any node within the right subtree.
// c. Postorder Traversal (left-right-current) — Visit the current node after visiting all the nodes of left and right subtrees.


//2) Breadth-First Search (BFS) Algorithm has one variant:

// a. Level Order Traversal — Visit nodes level-by-level and left-to-right fashion at the same level.




